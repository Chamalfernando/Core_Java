package a_7_Loops;

public class B_3_DoWhileLoops {
    public static void main(String[] args) {
        int i=1;
        do
        {
            System.out.println("Hi"+i);
            i++;
        }
        while(i<=4);

        int j = 6;
        do {
            System.out.println("hiiii" + j);
            j++;
        } while (j <= 4);

        System.out.println("Bye " + j);
    }
}

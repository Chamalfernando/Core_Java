package a_2_DataTypes;

public class First {
    public static void main(String[] args) {
        int num = 1_000_000_000;// You can use _ for the precision of a large number in java.
        int binary = 0b1011;
        int hex = 0x78E;
        double num1 = 12E10; // 12 into 10 raise to 10

        System.out.println(num);
        System.out.println(binary);
        System.out.println(hex);
        System.out.println(num1);
    }
}
